package info.keyal.demo.model;

/**
 * Item object
 */
public class Item {
    private String name;
    private double priceInPence;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPriceInPence() {
        return priceInPence;
    }

    public void setPriceInPence(double priceInPence) {
        this.priceInPence = priceInPence;
    }
}
